void TimDelayMicroSeconds( uint16_t microseconds)
{
   volatile uint32_t StartTime = htim2.Instance->CNT;
   volatile int32_t WaitTime = MICROSEC_TO_COUNT(microseconds); // Need to convert microseconds to timer counts
   volatile uint32_t CurrentTime;

   do
   {
       CurrentTime = htim2.Instance->CNT;
       if (CurrentTime < StartTime)
       {
           // Rolled over
           WaitTime -= (CurrentTime + 0xFFFFFFFF-StartTime);
       }
       else
       {
           WaitTime -= (CurrentTime-StartTime);
       }
       StartTime = CurrentTime;

   } while (WaitTime > 0);
}

static bool MeasTimerRunning;
static uint16_t RollCount = 0;
static uint16_t LastCount = 0;
static uint16_t SavedRollCount = 0;
static uint16_t SavedLastCount = 0;

void TimEnableMeasureTimer( uint32_t microseconds)
{
    uint32_t timeout = MICROSEC_TO_COUNT(microseconds);

    if (MeasTimerRunning)
    {
        TimDisableMeasureTimer();
    }

    /*
    SavedRollCount = RollCount = (uint16_t)(timeout >> 16);  // Max timeout value for RollCount number of interrupts, then followed by a lastcount;
    SavedLastCount = LastCount = (uint16_t)timeout;

    if (RollCount > 0)
    {
        timeout = 0xFFFF;
    }
    else
    {
        timeout = LastCount;
    }
    */

    HAL_TIM_Base_DeInit(&htim5);

    // Set the new timer period
    htim5.Init.Period = timeout;

    HAL_TIM_Base_Init(&htim5);

    HAL_TIM_Base_Start(&htim5);

    HAL_TIM_Base_Start_IT(&htim5);

}

void TimDisableMeasureTimer(void)
{
    MeasTimerRunning = false;

    HAL_TIM_Base_Stop_IT(&htim5);

}

bool TimCheckMeasureTimerTimeout(void)
{
    bool ret = false;;

    if (MeasTimerRunning)
    {
        if (RollCount > 1)
        {
            RollCount--;
        }
        else if (RollCount == 1)
        {
            RollCount--;
            if (LastCount > MICROSEC_TO_COUNT(50))
            {
                // Set counter to LastCount
                htim3.Init.Period = LastCount;
            }
            else
            {
                ret = true;   // With less then 50 microseconds to timeout there is little point in reloading the timer
            }

        }
        else
        {
            ret = true;
        }
    }

    if (ret)
    {
        // When we time out we reload the timer counters
        LastCount = SavedLastCount;
        RollCount = SavedRollCount;
        htim3.Init.Period = RollCount > 0 ? 0xFFFF : LastCount;
    }

    return(ret);
}


