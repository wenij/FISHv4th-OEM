#ifdef STM32F205xx
#define MICROSEC_TO_COUNT(_x)  ((_x) * 59)  // * APB1 Timer Clock in MHz
#else
#define MICROSEC_TO_COUNT(_x)  ((_x) * 84)  // * APB1 Timer Clock in MHz
#endif

